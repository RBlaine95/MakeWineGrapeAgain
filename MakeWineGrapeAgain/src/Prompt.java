    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        switch (Pinwheel.getBounce()) {
            case "edit":
                switch (Pinwheel.getSearchType()) {
                    case "batch":
                        Pinwheel.deleteBatch();
                        Pinwheel.insertBatch();
                        break;
                    case "supplier":
                        Pinwheel.deleteSupplier();
                        Pinwheel.insertSupplier();
                        break;
                    case "chemical":
                        Pinwheel.deleteChemical();
                        Pinwheel.insertChemical();
                        break;
                }
            case "delete":
                switch (Pinwheel.getSearchType()) {
                    case "batch":
                        Pinwheel.deleteBatch();
                        break;
                    case "subbatch":
                        Pinwheel.deleteSubBatch();
                        break;
                    case "blend":
                        Pinwheel.deleteBlend();
                        break;
                    case "supplier":
                        Pinwheel.deleteSupplier();
                        break;
                    case "chemical":
                        Pinwheel.deleteChemical();
                }
        }
        this.dispose();
    }//GEN-LAST:event_okBtnActionPerformed

